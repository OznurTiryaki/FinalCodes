package classCodes.f1_Intro;

public class VariableDeclaration {

    public static void main(String[] args) {

        /* Variable naming
         * letter, numbers, _ ,$
         * **cannot start with numbers
         *  recommendation if it is two name: Camel Case
         */
        //Variable declaration
        int myAge;

        //Assignment

        myAge = 453;

        System.out.println(myAge);

        //double
        double d = 45.1;
        System.out.println(d);

        //char
        char gChar = 'g';
        System.out.println(gChar);

        //boolean: two values: true/false
        boolean isDoorOpen;
        isDoorOpen = true;
        System.out.println(isDoorOpen);
    }
}
