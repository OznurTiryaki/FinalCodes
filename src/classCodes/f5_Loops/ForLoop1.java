package classCodes.f5_Loops;

public class ForLoop1 {

    public static void main(String[] args) {

        for (int i = 0 ; i < 5 ; i ++) {

            System.out.println("Okan Happy Birthday! "+ i);

        }
        for (int x=0 ; x<= 10 ; x++ ) {


            System.out.print(  x + " ");

        }
        System.out.println();
        for (int y=10 ; y >= 0 ; y-- ) {

            System.out.print(y + " ");

        }

    }
}