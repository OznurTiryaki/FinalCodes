package classCodes.f5_Loops;

public class WhileLoop14 {

    public static void main(String[] args) {

        String star = "*";

        int x = 0;

        while (x < 5) {


            System.out.println(star);

            star = star + "*";

            x++;
        }
        //System.out.println( star);
    }
}
